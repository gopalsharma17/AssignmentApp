/***
 * ListItem used in app for creating ListItem type array list
 */
public class ListItem {
    private String strRowTitel;
    private String strRowDiscription;
    private String imgURL;

    public String getStrRowTitel() {
        return strRowTitel;
    }

    public void setStrRowTitel(String strRowTitel) {
        this.strRowTitel = strRowTitel;
    }

    public String getStrRowDiscription() {
        return strRowDiscription;
    }

    public void setStrRowDiscription(String strRowDiscription) {
        this.strRowDiscription = strRowDiscription;
    }

    public String getImgURL() {
        return imgURL;
    }

    public void setImgURL(String imgURL) {
        this.imgURL = imgURL;
    }


}
